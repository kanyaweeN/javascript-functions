const add = (a, b) => {
    return a + b;
  };
  
  const subtract = (a, b) => {
    return a - b;
  };
  
  const multiply = (a, b) => {
    return a * b;
  };
  
  const divide = (a, b) => {
    return a / b;
  };
  
  console.log(add(10, 5));
  console.log(subtract(10, 5));
  console.log(multiply(10, 5));
  console.log(divide(10, 5));